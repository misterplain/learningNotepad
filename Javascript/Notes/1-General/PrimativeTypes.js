// 1. Undefined: Represents a variable that has been declared but not assigned a value.
// 2. Null: Represents an intentional absence of any object value.
// 3. Boolean: Represents true or false values.
// 4. Number: Represents numeric values.
// 5. String: Represents a sequence of characters.

// Primitive types are immutable, meaning their values cannot be changed once created.

// Examples:
let undefinedVar; // Undefined
let nullVar = null; // Null
let isTrue = true; // Boolean
let age = 25; // Number
let greeting = "Hello, World!"; // String
