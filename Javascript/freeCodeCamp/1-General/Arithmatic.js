// Addition (+)
let additionResult = 5 + 3; // 8

// Subtraction (-)
let subtractionResult = 10 - 4; // 6

// Multiplication (*)
let multiplicationResult = 6 * 7; // 42

// Division (/)
let divisionResult = 9 / 3; // 3

// Modulus (Remainder) (%)
let modulusResult = 10 % 3; // 1 (remainder of 10 divided by 3)

// Increment (++) and Decrement (--)
let x = 5;
x++; // Increment x by 1, x is now 6
x--; // Decrement x by 1, x is now 5

// Order of Operations (Parentheses)
let orderOfOperationsResult = (4 + 3) * 2; // 14 (first the addition, then multiplication)

// Assignment with Arithmetic Operators (+=, -=, *=, /=, %=)
let a = 10;
a += 5; // Equivalent to a = a + 5, a is now 15

// Unary Plus (+) and Minus (-)
let numberString = "42";
let unaryPlusResult = +numberString; // Converts to a number, unaryPlusResult is 42
let unaryMinusResult = -42; // Unary minus, unaryMinusResult is -42
